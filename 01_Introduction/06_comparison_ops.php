<?php 

/**
 * var_dump es una función que nos sirve para revisar
 * el interior de una variable para tener su valor original.
 * Su resultado siempre es en una sola línea.
 */

var_dump(2 > 1); // True

var_dump(3 < 2); // False

var_dump(1 == 1); // True

var_dump(3 >= 3); // True

var_dump(4 <= 5); // True

echo "\n";

